def sets():
    # Creating sets
    set1 = {1, 2, 3, 4, 5}
    set2 = {4, 5, 6, 7, 8}

    # Basic set operations
    print("Set 1:", set1)
    print("Set 2:", set2)

    # Union
    print("Union of Set 1 and Set 2:", set1 | set2)

    # Intersection
    print("Intersection of Set 1 and Set 2:", set1 & set2)

    # Difference
    print("Difference of Set 1 and Set 2:", set1 - set2)

    # Symmetric difference
    print("Symmetric Difference of Set 1 and Set 2:", set1 ^ set2)
  
