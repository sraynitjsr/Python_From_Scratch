def data_types():
    # Integer
    num_int = 42
    print("Integer Example:", num_int)

    # Float
    num_float = 3.14
    print("Float Example:", num_float)

    # String
    str_example = "Hello, World!"
    print("String Example:", str_example)

    # Boolean
    bool_example = True
    print("Boolean Example:", bool_example)

    # List
    list_example = [1, 2, 3, 4, 5]
    print("List Example:", list_example)

    # Tuple
    tuple_example = (1, 2, 3, 4, 5)
    print("Tuple Example:", tuple_example)

    # Dictionary
    dict_example = {'a': 1, 'b': 2, 'c': 3}
    print("Dictionary Example:", dict_example)
